import { IRepository } from '../../redux/types/repository';

export interface IProps {

  /**
   |--------------------------------------------------------------------------
   | Url. (Required)
   |--------------------------------------------------------------------------
   */
  html_url: string;

  /**
   |--------------------------------------------------------------------------
   | Name. (Required)
   |--------------------------------------------------------------------------
   */
  name: IRepository['name'];

  /**
   |--------------------------------------------------------------------------
   | Description. (Not required)
   |--------------------------------------------------------------------------
   */
  description?: IRepository['description'];

  /**
   |--------------------------------------------------------------------------
   | Owner. (Required)
   |--------------------------------------------------------------------------
   */
  owner: IRepository['owner'];

  /**
   |--------------------------------------------------------------------------
   | Add repository in Recent Search. (React redux)
   |--------------------------------------------------------------------------
   */
  addRepository: (repository: IRepository) => void;

}